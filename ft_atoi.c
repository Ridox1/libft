#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		sign;
	long	nb;

	i = 0;
	sign = 1;
	nb = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (nb * 10 + (str[i] - 48) < nb && sign == -1)
			return (0);
		else if (nb * 10 + (str[i] - 48) < nb && sign == 1)
			return (-1);
		nb = nb * 10 + (str[i++] - 48);
	}
	return (nb * sign);
}
